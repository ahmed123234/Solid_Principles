class Book {

	String name;
	String authorName;
	Integer year;
	Integer price;
	Integer isbn;


	public Book(String name, String authorName, Integer year, Integer price, Integer isbn) {
		this.name = name;
		this.authorName = authorName;
		this.year = year;
		this.price = price;
		this.isbn = isbn;
	}
}
